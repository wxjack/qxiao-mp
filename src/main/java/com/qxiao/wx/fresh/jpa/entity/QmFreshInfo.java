package com.qxiao.wx.fresh.jpa.entity;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
@Entity
public class QmFreshInfo {
	@Id
	@GeneratedValue
	private Long freshId; 
	private String title;
	private String textContent;
	private String openId;
	private int messageSend;
	private int isDel;
	private Date sendTime;
	private Date postTime;
	
	public Long getFreshId() {
		return freshId;
	}
	public void setFreshId(Long freshId) {
		this.freshId = freshId;
	}
	public String getOpenId() {
		return openId;
	}
	public void setOpenId(String openId) {
		this.openId = openId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getTextContent() {
		return textContent;
	}
	public void setTextContent(String textContent) {
		this.textContent = textContent;
	}
	public int getIsDel() {
		return isDel;
	}
	public void setIsDel(int isDel) {
		this.isDel = isDel;
	}
	public Date getPostTime() {
		return postTime;
	}
	public void setPostTime(Date postTime) {
		this.postTime = postTime;
	}
	public int getMessageSend() {
		return messageSend;
	}
	public void setMessageSend(int messageSend) {
		this.messageSend = messageSend;
	}
	public Date getSendTime() {
		return sendTime;
	}
	public void setSendTime(Date sendTime) {
		this.sendTime = sendTime;
	}
	
}
